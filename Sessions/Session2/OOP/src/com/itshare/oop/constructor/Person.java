package com.itshare.oop.constructor;

public class Person {

	private String name;   //s
	private int age;
	private int salary;
	
	
	
	public void setName(String n) {
		name = n;
	}
	
	public String getName() {
		
		return name;
	}
	
	public void setAge(int a) {
		age = a;
	}
	
	public int getAge() {
		
		return age;
	}
	
	public void setSalary(int s) {
		salary = s;
	}
	
	public int getSalary() {
		
		return salary;
	}
	
}
